#include<iostream>
using namespace std;
int main(){
   //Integer data type
   int apples=5;
   cout<<apples<<endl;
   cout<<"Size of int:"<<sizeof(apples)<<endl;

   //Character data type
   char sign='&';
   cout<<sign<<endl;
   cout<<"Size of char:"<<sizeof(sign)<<endl;

   //Boolean data type
   bool flag=true;
   cout<<flag<<endl;
   cout<<"size of bool:"<<sizeof(bool)<<endl;

   //Float data type
   float score=3.45657;
   cout<<score<<endl;
   cout<<"Size of float:"<<sizeof(score)<<endl;

   //Double data type
   double d=7983944910;
   cout<<d<<endl;
   cout<<"Size of double:"<<sizeof(d)<<endl;
   return 9;
}